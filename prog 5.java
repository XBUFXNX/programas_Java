// ciclo for each
package CiclosFor; 

import java.util.ArrayList;

public class foreach { 
	public static void main(String[] args) { 
		int[] valor = {1, 2, 3, 4, 5, 6, 7, 19, 10}; //array de numeros/int, con varios valores de prueba
		for(int i : valor ) System.out.println(i); //ciclo for each que le asigna los valores que se van recorriendo a la variable i
		
	}
}
